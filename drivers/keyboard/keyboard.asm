[bits 32]

; Порты контроллера PS/2
PS2_DATA_PORT equ 0x60
PS2_CMD_PORT  equ 0x64

; Базовый массив трансляции скан-кодов (Set 1) в ASCII
; Для примера добавлена только верхняя часть стандартной QWERTY-клавиатуры
scancode_to_ascii:
    db 0, 27, '1', '2', '3', '4', '5', '6', '7', '8', '9', '0', '-', '=', 8, 9
    db 'q', 'w', 'e', 'r', 't', 'y', 'u', 'i', 'o', 'p', '[', ']', 10, 0
    ; ... здесь таблица продолжается для остальных клавиш ...

global keyboard_handler_main

; Эта функция должна вызываться из обработчика прерывания IRQ1 (isr_keyboard)
keyboard_handler_main:
    pusha

    ; 1. Проверяем статус буфера контроллера
    in al, PS2_CMD_PORT
    test al, 1          ; Бит 0 указывает, есть ли данные для чтения
    jz .done            ; Если 0, буфер пуст, выходим

    ; 2. Читаем скан-код
    in al, PS2_DATA_PORT

    ; 3. Фильтруем события
    ; Если установлен 7-й бит (0x80), это событие Key Release (клавиша отпущена)
    ; В базовом драйвере мы обрабатываем только Key Press
    test al, 0x80       
    jnz .done

    ; 4. Трансляция скан-кода в ASCII
    movzx ebx, al                     ; Очищаем EBX и кладем туда скан-код
    mov al, [scancode_to_ascii + ebx] ; Достаем символ из таблицы по смещению

    ; -> На этом этапе в регистре AL лежит готовый ASCII символ.
    ; -> Здесь обычно вызывается функция терминала для отрисовки символа (например, vga_print_char)

.done:
    popa
    ret
