; ═══════════════════════════════════════════════════════════════
;  BOOTLOADER #01 — Minimal Real Mode
;  Режим:    16-bit Real Mode
;  Цель:     Инициализация, вывод строки через BIOS INT 10h
;  Сборка:   nasm boot01_minimal.asm -f bin -o boot01.bin
;  Тест:     qemu-system-x86_64 -drive format=raw,file=boot01.bin
; ═══════════════════════════════════════════════════════════════

BITS 16
ORG  0x7C00

; ── Инициализация сегментов ──────────────────────────────────
start:
    cli                  ; Отключаем прерывания на время инициализации
    xor  ax, ax          ; AX = 0 (xor быстрее чем mov ax, 0)
    mov  ds, ax          ; Сегмент данных = 0
    mov  es, ax          ; Доп. сегмент  = 0
    mov  ss, ax          ; Сегмент стека = 0
    mov  sp, 0x7C00      ; Стек растёт вниз от 0x7C00 (ниже бутсектора)
    sti                  ; Разрешаем прерывания

; ── Вывод строки через BIOS INT 10h ─────────────────────────
    mov  si, msg_hello   ; SI = адрес строки

.print_loop:
    lodsb                ; AL = byte[SI], SI++
    test al, al          ; Проверяем нуль-терминатор
    jz   .halt           ; Конец строки — стоп
    mov  ah, 0x0E        ; BIOS функция: вывод символа (TTY mode)
    mov  bh, 0x00        ; Номер страницы = 0
    mov  bl, 0x07        ; Цвет текста (светло-серый на чёрном)
    int  0x10            ; Вызов BIOS Video Services
    jmp  .print_loop

; ── Безопасная остановка ─────────────────────────────────────
.halt:
    cli                  ; Запрещаем прерывания
    hlt                  ; Останавливаем процессор
    jmp  .halt           ; На случай NMI — снова в hlt

; ── Данные ───────────────────────────────────────────────────
msg_hello db "Sentinel OS booting...", 0x0D, 0x0A
          db "Real Mode OK", 0x0D, 0x0A, 0

; ── Заполнение до 510 байт + сигнатура MBR ──────────────────
times 510 - ($ - $$) db 0   ; Нули до конца сектора
dw 0xAA55                    ; Магическая подпись: BIOS проверяет её!
