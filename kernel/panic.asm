; core/panic.asm
global panic

section .text
; Вход: ESI = адрес строки с сообщением (нулевой терминатор)
panic:
    cli                 ; Отключаем прерывания, чтобы не мешали
    mov edi, 0xB8000    ; Адрес VGA-буфера
    mov ah, 0x4F        ; Атрибуты: красный фон (4), белый текст (F)
    
.loop:
    lodsb               ; Читаем байт из строки (в AL)
    cmp al, 0           ; Конец строки?
    je .halt
    stosw               ; Записываем символ + атрибут в видеопамять
    jmp .loop

.halt:
    hlt                 ; Остановка процессора
    jmp .halt           ; На случай, если процессор проснется
